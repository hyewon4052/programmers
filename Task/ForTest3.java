class ForTest3 
{
	public static void main(String[] args) 
	{
		int i,j;
		System.out.println("홀수:");
		for (i = 1; i <=20 ; i++ )
		{
			System.out.println(i);
			i = i + 1;
		}
		System.out.println("");
		System.out.println("짝수:");
		for (j = 2; j <=20 ; j++ )
		{
			System.out.println(j);
			j = j + 1;
		}
	}
}
